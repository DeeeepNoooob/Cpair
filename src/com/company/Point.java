public class Point {
    int x,y;
    int ID;

    public Point(int x,int y,int ID){
        this.x=x;
        this.y=y;
        this.ID=ID;
    }

    public int getY() {
        return y;
    }

    public int getX() {

        return x;
    }

    public int getID() {
        return ID;
    }
}
